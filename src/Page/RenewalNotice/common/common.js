export const getToken=  ()=>{
 return  axios.post(oAuthURl,
     `grant_type=${tokenCred.grantType}&client_id=${tokenCred.clientId}&client_secret=${tokenCred.clientSecret}`)
     .then(res => {
      if (res.status == 200) {
        localStorage.awsToken = JSON.stringify(res.data);
        // localStorage.setItem("awsToken",JSON.stringify(res.data))
            return   {
               "Authorization": res.data.token_type + ' ' + res.data.access_token,
               "Content-Type": "application/json",
            }
          }else{
            return {
               "Content-Type": "application/json",
            }
          }
        })
   }  

   export const apiService =  (method, url, body) => {
   
    const  access_list_fr = JSON.parse(localStorage.awsToken);
 
     return  axios({
       method: method,
       baseURL: BaseURl,
       url: url,
       headers:  {
        "Authorization": access_list_fr.token_type + ' ' + access_list_fr.access_token,
        "Content-Type": "application/json",
     },
       data: body,
     })
       .then((e) => {
      
         return e;
       })
       .catch((error) => {
        
        if (!error.status) {
          getToken()
      // window.location.href ="#/LoginView"
        }
    
         return error;
       });
   };

